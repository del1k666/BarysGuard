_LANG_NAMES = {"eng": "ENG", "rus": "RUS", "kaz": "ҚАЗ"}
_SUPPORTED  = list(_TRANSLATIONS.keys())


def current_lang() -> str:
    return Config.get("language", "rus")


def set_lang(lang: str) -> None:
    if lang in _TRANSLATIONS:
        Config.set("language", lang)


def t(key: str, **fmt) -> str:
    lang = current_lang()
    text = _TRANSLATIONS.get(lang, {}).get(key) or _TRANSLATIONS["rus"].get(key, key)
    return text.format(**fmt) if fmt else text


def lang_names() -> dict:
    return dict(_LANG_NAMES)
